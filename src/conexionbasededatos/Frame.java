    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
DefaultTableModel model=(DefaultTableModel)this.tabla.getModel();
int i = tabla.getSelectedRow();
//condicion si no se selecciono ninguno
if(i==-1){
JOptionPane.showMessageDialog(this, "Seleccione");
}else{
//mostramso los datos en los jtxt
    //convertimos en string el resultado
    txtnombre.setText(String.valueOf(tabla.getValueAt(i, 0)));
    txtedad.setText(String.valueOf(tabla.getValueAt(i, 1)));
    txtcorreo.setText(String.valueOf(tabla.getValueAt(i, 2)));        
    txtcontraseña.setText(String.valueOf(tabla.getValueAt(i, 3)));
    }//GEN-LAST:event_tablaMouseClicked
}
