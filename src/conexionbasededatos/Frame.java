    //metodo para AGREGAR DATOS
    public void Agregar(){
    Metodos met = new Metodos();
    met.AgregarConsulta(txtnombre.getText(), Integer.parseInt(txtedad.getText()), txtcorreo.getText(), txtcontraseña.getText());
    DatosTabla();
    }
    //metodo para eliminar datos
    public void Eliminar(){
    Metodos met = new Metodos();
    met.EliminarConsulta(txtnombre.getText());
    DatosTabla();
    }
    //metodo para modificar datos
    public void Modificar(){
    Metodos met = new Metodos();
    //en el mismo orden que el metodo en operaciones
    met.ModificarConsulta(txtnombre.getText(), Integer.parseInt(txtedad.getText()), txtcorreo.getText(), txtcontraseña.getText());
    DatosTabla();
    }

